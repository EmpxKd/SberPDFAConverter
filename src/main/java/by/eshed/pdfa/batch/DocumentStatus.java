package by.eshed.pdfa.batch;

/**
 * Состояние одного документа батча. Документ - одна группа файлов с одинаковым {@code docId},
 * конвертируемая в один PDF/A-1 воркером пула.
 */
public enum DocumentStatus {
    /** Поставлен в очередь, обработка ещё не началась. */
    PENDING,
    /** Воркер пула выполняет конвертацию прямо сейчас. */
    RUNNING,
    /** Конвертация завершена успешно, результат лежит в {@code ResultStore}. */
    DONE,
    /** Конвертация упала - смотри {@code DocumentJob#error()} для причины; батч продолжается. */
    FAILED
}
